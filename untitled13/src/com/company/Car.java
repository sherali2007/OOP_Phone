package com.company;

public class Car {
    String brand;
    String name;
    String color;
    int year;
    String price;

    public Car() {
    }

    public Car(String brand, String name, String color, int year, String price) {
        this.brand = brand;
        this.name = name;
        this.color = color;
        this.year = year;
        this.price = price;
    }
}
